function interpolate(message, parameters = {}) {
    return String(message).replace(/\{(\w+)\}/g, (match, key) => (
        Object.hasOwn(parameters, key) ? parameters[key] : match
    ));
}

export function resolveLocale(locale) {
    return SUPPORTED_LOCALES.some((candidate) => candidate.code === locale)
        ? locale
        : DEFAULT_LOCALE;
}

export function createI18n(locale = DEFAULT_LOCALE) {
    const resolvedLocale = resolveLocale(locale);
    const localeDefinition = SUPPORTED_LOCALES.find(
        (candidate) => candidate.code === resolvedLocale,
    );
    const messages = { ...ENGLISH, ...(MESSAGES[resolvedLocale] || {}) };
    const themes = { ...THEME_LABELS.en, ...(THEME_LABELS[resolvedLocale] || {}) };

    return {
        locale: resolvedLocale,
        direction: localeDefinition.direction,
        t(key, parameters) {
            return interpolate(messages[key] || ENGLISH[key] || key, parameters);
        },
        themeLabel(themeId, fallback = themeId) {
            return themes[themeId] || THEME_LABELS.en[themeId] || fallback;
        },
        formatNumber(value, digits = 1) {
            if (value === null || value === undefined || !Number.isFinite(Number(value))) {
                return "—";
            }
            return new Intl.NumberFormat(resolvedLocale, {
                maximumFractionDigits: digits,
            }).format(Number(value));
        },
        formatPercent(value, digits = 1) {
            if (value === null || value === undefined || !Number.isFinite(Number(value))) {
                return "—";
            }
            return new Intl.NumberFormat(resolvedLocale, {
                style: "percent",
                maximumFractionDigits: digits,
            }).format(Number(value) / 100);
        },
        formatDate(value) {
            const date = value instanceof Date ? value : new Date(value);
            return new Intl.DateTimeFormat(resolvedLocale, {
                dateStyle: "medium",
                timeStyle: "short",
            }).format(date);
        },
    };
}
